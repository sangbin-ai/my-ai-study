a = [10, 20, 30, 40, 50] 
print(a[0:3]) #0=첫 번째 인덱스, 3= 3-1반째 인덱스

alphabet = ["a", "b", "c", "d", "e"]
print(alphabet[2:]) #2=3번째 인덱스, 빈칸은 끝자리까지

text = "python"
print(text[:3]) # 빈칸은 처음부터, 3= 2번째 인덱스 문자까지 출력

text2 = "python"
print(text2[4:]) # -2는 뒤에서 2번째 부터 출력

# 리스트[start:end] → start부터 end-1까지 잘라냄

# :end → 처음부터 end-1까지

# start: → start부터 끝까지

# - 인덱스 → 뒤에서부터